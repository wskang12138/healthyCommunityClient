export function checkMail(rule, value, callback) {
  const regCN = /^\w+((-\w+)|(\.\w+))*\@[A-Za-z0-9]+((\.|-)[A-Za-z0-9]+)*\.[A-Za-z0-9]+$/
  if (!regCN.test(value)) {
    callback(new Error('邮箱格式不正确'))
  } else {
    callback()
  }
}

export function validateMobilePhone(rule, value, callback) {
  if (value === '') {
    callback(new Error('负责人手机号不可为空'))
  } else {
    if (value !== '') {
      const reg = /^1[3456789]\d{9}$/
      if (!reg.test(value)) {
        callback(new Error('请输入有效的手机号码'))
      }
    }
    callback()
  }
}
